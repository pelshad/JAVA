package sec02.exam01_byte;

public class byteExample {

	public static void main(String[] args) {
		//byte -128~127
		byte var1 = -128;//byte가 가질 수 있는 최소값
		byte var2 = -30;
		byte var3 = 0;
		byte var4 = 30;
		byte var5 = 127; //byte가 자길 수 있는 최대값
		
		//byte var6 = 128; byte범위 초과
		
		System.out.println(var1);
	}

}
